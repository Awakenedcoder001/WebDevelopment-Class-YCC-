def add(a,b):
    sum = a + b
    return sum

def subtract (a,b):
    dif = a - b
    return dif

def multiply (a,b):
    expo = a * b
    return expo

def division (a,b):
    quotient = a/b
    return quotient

if __name__ == '__main__':
    print(add(10,20))
    print(subtract(30,15))
    print(multiply(10,30))
    print(division(10,2))