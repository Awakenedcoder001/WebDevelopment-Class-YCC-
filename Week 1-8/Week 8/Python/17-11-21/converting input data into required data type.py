#Explicit conversion: 
#Converting String to Integer 
num1=int(input("Enter the integer number : ")) 
print(type(num1)) 
print(num1) 
 
#Converting String to Floating 
num1=float(input("Enter the floating number : ")) 
print(type(num1)) 
print(num1) 
 
#Converting String to Complex 
num1=complex(input("Enter the complex number : ")) 
print(type(num1)) 
print(num1) 
 
#Converting String to Boolean Value 
num1=bool(input("Enter the  True or False : ")) 
print(type(num1)) 
print(num1)