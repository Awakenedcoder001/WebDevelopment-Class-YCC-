# Strict Formatting:

# interger formating -- > %d
# octal formatting --> %o
# HExadecimal formatting --> %x
# floating point --> %f
# string formatting --> %s

a=10
print("The value of a is = %d" %a)
print("The value of a is = %f" %a)
print("The value of a is = %.2f" %a)

b=20.5
print("The value of b is = %d" %b)
print("The value of b is = %f" %b)
print("The value of b is = %.2f" %b)

s="Welcome to Bhutan"
print("The value of the string is = %s" %s)
